    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (!txtNombres.getText().isEmpty()
                &&!txtApellidos.getText().isEmpty()
                &&!txtCorreo.getText().isEmpty()&&!txtUsuario.getText().isEmpty()&&!txtContrasenia.getText().isEmpty()) {
           Usuario usuario= new Usuario(txtUsuario.getText(),txtContrasenia.getText(),txtNombres.getText(),txtApellidos.getText(),txtCorreo.getText());
           if(UsuarioLogic.insertar(usuario)){
               JOptionPane.showMessageDialog(this, "Usuario registrado exitosamente");
           }else{
               JOptionPane.showMessageDialog(this, "Usuario inexistente");
           }
            
            
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (!txtNombres.getText().isEmpty()
                &&!txtApellidos.getText().isEmpty()
                &&!txtCorreo.getText().isEmpty()&&!txtUsuario.getText().isEmpty()&&!txtContrasenia.getText().isEmpty()) {
           Usuario usuario= new Usuario(txtUsuario.getText(),txtContrasenia.getText(),txtNombres.getText(),txtApellidos.getText(),txtCorreo.getText());
           if(UsuarioLogic.modificar(usuario)){
               JOptionPane.showMessageDialog(this, "Usuario modificado exitosamente");
           }else{
               JOptionPane.showMessageDialog(this, "Usuario no encontrado");
           }
            
            
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       if (!txtUsuario.getText().isEmpty()) {
           Usuario usuario= new Usuario(txtUsuario.getText(),txtContrasenia.getText(),txtNombres.getText(),txtApellidos.getText(),txtCorreo.getText());
           if(UsuarioLogic.eliminar(txtUsuario.getText())){
               JOptionPane.showMessageDialog(this, "Usuario eliminado exitosamente");
           }else{
               JOptionPane.showMessageDialog(this, "Usuario no encontrado");
           }
            
            
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

